import { type NextPage } from "next";
import Head from "next/head";
import { useState } from "react";
import { DateRangePicker } from "react-date-range";
import {
  MagnifyingGlassIcon,
  GlobeAltIcon,
  Bars3Icon,
  UserCircleIcon,
  UsersIcon,
} from "@heroicons/react/24/solid";
import Footer from "@/components/Footer";
import "react-date-range/dist/styles.css"; // main style file
import "react-date-range/dist/theme/default.css"; // theme css file

const Home: NextPage = () => {
  const [searchInput, setSearchInput] = useState("");
  const [startDate, setStartDate] = useState(new Date());
  const [endDate, setEndDate] = useState(new Date());
  const selectionRange = {
    startDate: startDate,
    endDate: endDate,
    key: "selection",
  };

  const handleSelect = (ranges) => {
    setStartDate(ranges.selection.startDate);
    setEndDate(ranges.selection.endDate);
  };
  return (
    <>
      <Head>
        <title>Car rental App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex min-h-screen flex-col items-center justify-center">
        <div className="flex bg-red-500 ">Hi hello</div>
        {/* MIDDLE SEARCH*/}
        <div className="flex items-center rounded-full py-2 md:border-2 md:shadow-sm">
          <input
            type="text"
            placeholder={"Start your search"}
            className="flex-grow bg-transparent pl-5 outline-none"
            onChange={(e) => setSearchInput(e.target.value)}
          />
          {/* 768px - search icon alga bolno. */}
          <MagnifyingGlassIcon className="hidden h-8 cursor-pointer rounded-full bg-red-400 p-2 text-white md:mx-2 md:inline-flex" />
        </div>
        <div className="col-span-3 mx-auto flex flex-col">
          <DateRangePicker
            ranges={[selectionRange]}
            minDate={new Date()}
            rangeColors={["#FD5B61"]}
            onChange={handleSelect}
            staticRanges={[]}
            inputRanges={[]}
          />
        </div>
        <Footer />
      </main>
    </>
  );
};

export default Home;
